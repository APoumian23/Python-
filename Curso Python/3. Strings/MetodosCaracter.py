cadena = 'Estoy utilizando los metodos de Python'

print(cadena.lower()) #Toda mayuscula se convierte en minuscula
print(cadena.upper()) #Todo el escrito se convierte a mayuscula
print(cadena.capitalize()) #La inicial de la oracion se vuelve mayuscula
print(cadena.title()) #La inicial de cada palabra se vuelve mayuscula
print(cadena.swapcase()) #Intercambia de minuscual a mayuscula y viceversa